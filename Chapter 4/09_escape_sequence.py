# Escape Sequence

# \n : for new line 
a = "He is a good boy\nbut not a bad boy"
print(a)

# \t : for TAB
a = "He is a good boy\tbut not a bad boy"
print(a)

# \" : for double quote or single quote 
a = "He is a good boy \"but\" not a bad boy"
print(a)

# \\ : for backslash
a = "He is a good boy\\but not a bad boy" 
print(a)